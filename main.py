def Matrix18(matrix):
    """Знаходимо кількість стовпців з різними елементами і додаємо до матриці рядків M з випадковими числами"""
    M, N = matrix.shape # отримуємо розміри матриці
    unique_cols = sum(1 for col in matrix.T if len(set(col)) == len(col)) # вважаємо кількість стовпців, у яких всі елементи різні
    print(f"\nКількість стовпців з різними елементами: {unique_cols} \n") # виводимо результат
    random_rows = np.random.randint(1, 100, size=(M, N)) # створюємо M рядків із випадковими числами від 1 до 100
    new_matrix = np.concatenate([matrix, random_rows], axis=0) # об'єднуємо вихідну матрицю та нові рядки по вертикалі
    print("Нова матриця після додавання випадкових рядків: \n") # виводимо повідомлення
    print(new_matrix) # виводимо нову матрицю

