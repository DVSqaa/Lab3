def SortDec3(A, B, C):
  """Сортуємо A, B, C за зменшенням"""
  lst = sorted([A, B, C], reverse=True) # створюємо список з A, B, C та сортуємо його у зворотному порядку
  return lst[0], lst[1], lst[2] # повертаємо перший, другий та третій елементи списку

def Proc13():
  """Сортуємо два набори з трьох чисел за спаданням"""
  A1, B1, C1 = map(int, input("\nВведіть три числа для першого набору (через пропуск): ").split()) # зчитуємо три числа для першого набору і перетворюємо їх на цілі
  A2, B2, C2 = map(int, input("\nВведіть три числа для другого набору (через пропуск): ").split()) # зчитуємо три числа для другого набору і перетворюємо їх на цілі
  A1, B1, C1 = SortDec3(A1, B1, C1) # сортуємо перший набір за спаданням
  A2, B2, C2 = SortDec3(A2, B2, C2) # сортуємо другий набір за спаданням
  print(f"\nПерший набір, відсортований за зменшенням: {A1}, {B1}, {C1} ") # виводимо перший набір
  print(f"Другий набір, відсортований за зменшенням: {A2}, {B2}, {C2} \n") # виводимо другий набір
Proc13() # Викликаємо функцію Proc13


